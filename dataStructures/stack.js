// A stack is a data structure that follows the Last In First Out (LIFO) principle, meaning that the last item added to the stack is the first item to be removed.
// Stacks are iterable, meaning they can be looped over with a for-of loop.
// Stacks have a size property, which returns the number of items in the stack.
// Stacks have a push() method, which adds a new item to the top of the stack.
// Stacks have a pop() method, which removes the top item from the stack and returns it.
// Stacks have a peek() method, which returns the top item from the stack without removing it.
// Stacks have an isEmpty() method, which returns a boolean indicating whether the stack is empty.

